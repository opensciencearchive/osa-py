_GITIGNORE_LINES = [".data/", ".env", ".osa/"]


def init_project(
    *,
    project_dir: Path,
    name: str | None = None,
    force: bool = False,
) -> InitResult:
    """Scaffold a new OSA project directory."""
    project_dir.mkdir(parents=True, exist_ok=True)

    if (project_dir / "osa.yaml").exists() and not force:
        raise InstanceError("Project already initialized. Use --force to overwrite.")

    resolved_name = name or project_dir.name

    # Write osa.yaml
    (project_dir / "osa.yaml").write_text(_OSA_YAML_TEMPLATE.format(name=resolved_name))

    # Write .env with generated secrets
    (project_dir / ".env").write_text(_ENV_TEMPLATE)

    # Create directories
    (project_dir / ".data").mkdir(exist_ok=True)
    (project_dir / ".osa").mkdir(exist_ok=True)

    # Write or append .gitignore
    gitignore_path = project_dir / ".gitignore"
    existing = gitignore_path.read_text() if gitignore_path.exists() else ""
    existing_lines = existing.splitlines()
    to_add = [line for line in _GITIGNORE_LINES if line not in existing_lines]
    if to_add:
        separator = "\n" if existing and not existing.endswith("\n") else ""
        gitignore_path.write_text(existing + separator + "\n".join(to_add) + "\n")

    return InitResult(
        project_dir=project_dir,
        created=[
            ("osa.yaml", "archive configuration"),
            (".env", "secrets and settings (do not commit)"),
            (".data/", "persistent data directory"),
            (".osa/", "CLI state"),
            (".gitignore", "ignores .data/, .env, .osa/"),
        ],
        show_cd=project_dir.resolve() != Path.cwd().resolve(),
    )


def _build_compose_command(
    *,
    project_dir: Path,
    project_name: str,
    source: Path | None = None,
    profiles: list[str] | None = None,
) -> list[str]:
    cmd = [
        "docker",
        "compose",
        "--project-name",
        project_name,
        "--project-directory",
        str(project_dir),
        "-f",
        str(_compose_template_path()),
    ]

    if source is not None:
        dev_override = _write_dev_override(source=source, project_dir=project_dir)
        cmd.extend(["-f", str(dev_override)])

    override_path = project_dir / "docker-compose.override.yml"
    if override_path.exists():
        cmd.extend(["-f", str(override_path)])

    for profile in profiles or []:
        cmd.extend(["--profile", profile])

    cmd.extend(["--env-file", str(project_dir / ".env")])

    return cmd


def _write_dev_override(*, source: Path, project_dir: Path) -> Path:
    source_abs = source.resolve()
    # The image's ENTRYPOINT is /app/scripts/entrypoint.sh and its CMD is the
    # uvicorn invocation. We override CMD to add --reload for hot-reload during
    # source-builds; the entrypoint (migrations + dev admin seed) is shared.
    override = {
        "services": {
            "server": {
                "build": {
                    "context": str(source_abs),
                    "dockerfile": "Dockerfile",
                    "target": "runtime",
                },
                "image": None,
                "command": [
                    "uvicorn",
                    "--factory",
                    "osa.application.api.rest.app:create_app",
                    "--host",
                    "0.0.0.0",
                    "--port",
                    "8000",
                    "--reload",
                ],
                "environment": {
                    "OSA_DEV_MODE": "true",
                },
            }
        }
    }
    dest = project_dir / ".osa" / "docker-compose.dev.yml"
    dest.parent.mkdir(parents=True, exist_ok=True)
    dest.write_text(yaml.dump(override, default_flow_style=False, sort_keys=False))
    return dest


def start_instance(
    *,
    project_dir: Path,
    detach: bool = True,
    source: Path | None = None,
    with_ui: bool = False,
    osa_version: str | None = None,
    ui: UI | None = None,
) -> None:
    ui = ui or UI.create()
    project_name = _read_project_name(project_dir)
    profiles = ["ui"] if with_ui else []

    if osa_version is not None:
        image_version = osa_version
    else:
        with ui.task("Resolving OSA version") as task:
            image_version = fetch_latest_osa_version()
            task.done(detail=image_version)

    cmd = _build_compose_command(
        project_dir=project_dir,
        project_name=project_name,
        source=source,
        profiles=profiles,
    )

    args = [*cmd, "up"]
    if source is not None:
        args.append("--build")
    if detach:
        args.append("-d")

    from osa.cli.link import write_link

    write_link(LOCAL_SERVER_URL, project_dir=project_dir)
    _store_dev_credentials()

    env = {**os.environ, "OSA_IMAGE_VERSION": image_version}

    if not detach:
        # Foreground compose is interactive/streaming — inherit stdio, no UI.
        ui.info(f"Starting OSA {image_version} (foreground, Ctrl-C to stop)")
        result = subprocess.run(args, cwd=project_dir, text=True, env=env)
        if result.returncode != 0:
            raise InstanceError("Failed to start OSA instance.")
        return

    with ui.task("Starting services") as task:
        proc = run_streamed(args, task=task, cwd=project_dir, env=env)
        if proc.returncode != 0:
            raise InstanceError(
                "Failed to start OSA instance",
                cause=tail(proc.output, 30),
                hint="Run `osa logs server --tail 50` for details",
            )
    ui.success(f"OSA {image_version} running", arrow=LOCAL_SERVER_URL)


def stop_instance(*, project_dir: Path, ui: UI | None = None) -> None:
    ui = ui or UI.create()
    project_name = _read_project_name(project_dir)
    cmd = _build_compose_command(project_dir=project_dir, project_name=project_name)
    with ui.task("Stopping services") as task:
        proc = run_streamed([*cmd, "down"], task=task, cwd=project_dir)
        if proc.returncode != 0:
            raise InstanceError(
                "Failed to stop OSA instance",
                cause=tail(proc.output, 30),
            )


def instance_logs(
    *,
    project_dir: Path,
    follow: bool = False,
    service: str | None = None,
    tail: int | None = None,
) -> subprocess.CompletedProcess[str]:
    project_name = _read_project_name(project_dir)
    cmd = _build_compose_command(project_dir=project_dir, project_name=project_name)
    args = [*cmd, "logs"]
    if follow:
        args.append("--follow")
    if tail is not None:
        args.extend(["--tail", str(tail)])
    if service:
        args.append(service)
    return subprocess.run(args, cwd=project_dir, text=True)


def instance_status(*, project_dir: Path) -> list[ComposeService]:
    project_name = _read_project_name(project_dir)
    cmd = _build_compose_command(project_dir=project_dir, project_name=project_name)
    result = subprocess.run(
        [*cmd, "ps", "--format", "json"],
        cwd=project_dir,
        text=True,
        capture_output=True,
    )
    if result.returncode != 0:
        raise InstanceError(
            "Failed to query instance status",
            cause=(result.stderr or result.stdout or "").strip() or None,
        )

    services: list[ComposeService] = []
    for line in (result.stdout or "").strip().splitlines():
        line = line.strip()
        if not line:
            continue
        data = json.loads(line)
        publishers = data.get("Publishers") or []
        ports = ", ".join(
            f"{p['PublishedPort']}->{p['TargetPort']}"
            for p in publishers
            if p.get("PublishedPort")
        )
        services.append(
            ComposeService(
                name=data.get("Service") or data.get("Name", ""),
                state=data.get("State", ""),
                health=data.get("Health", "") or "",
                ports=ports,
            )
        )
    return services
