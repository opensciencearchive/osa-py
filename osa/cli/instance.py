GHCR_IMAGE = "opensciencearchive/osa"
LOCAL_SERVER_URL = "http://127.0.0.1:8000"


def fetch_latest_osa_version() -> str:
    """Fetch the latest semver tag from GHCR for the OSA server image."""
    import httpx

    token_url = f"https://ghcr.io/token?scope=repository:{GHCR_IMAGE}:pull"
    tags_url = f"https://ghcr.io/v2/{GHCR_IMAGE}/tags/list"

    try:
        token_resp = httpx.get(token_url, timeout=10)
        token_resp.raise_for_status()
        token = token_resp.json()["token"]

        tags_resp = httpx.get(
            tags_url, headers={"Authorization": f"Bearer {token}"}, timeout=10
        )
        tags_resp.raise_for_status()
        tags = tags_resp.json()["tags"]
    except (httpx.HTTPError, KeyError) as e:
        raise InstanceError(
            "Could not fetch latest OSA version from the container registry",
            hint="Use --osa-version to specify manually",
        ) from e

    semver = [t for t in tags if re.match(r"^v\d+\.\d+\.\d+$", t)]
    if not semver:
        raise InstanceError(
            "No release tags found in the container registry",
            hint="Use --osa-version to specify manually",
        )

    semver.sort(key=lambda t: tuple(int(x) for x in t[1:].split(".")))
    return semver[-1]


class InstanceError(Exception):
    """Raised when a local instance operation fails."""

    def __init__(
        self, message: str, *, cause: str | None = None, hint: str | None = None
    ) -> None:
        super().__init__(message)
        self.cause = cause
        self.hint = hint


class InitResult(BaseModel):
    """Outcome of project scaffolding, rendered by the CLI layer."""

    project_dir: Path
    created: list[tuple[str, str]]
    show_cd: bool


class ComposeService(BaseModel):
    """One row of `docker compose ps` output."""

    name: str
    state: str
    health: str
    ports: str


def _compose_template_path() -> Path:
    ref = importlib.resources.files("osa.cli.templates").joinpath("docker-compose.yml")
    return Path(str(ref))


def _read_project_name(project_dir: Path) -> str:
    config_path = project_dir / "osa.yaml"
    if not config_path.exists():
        raise InstanceError(
            "osa.yaml not found. Run `osa init` first or cd into your project directory."
        )
    data = yaml.safe_load(config_path.read_text()) or {}
    name = data.get("name", project_dir.name)
    return re.sub(r"[^a-z0-9-]", "-", name.lower()).strip("-")


def _b64url(data: bytes) -> str:
    return base64.urlsafe_b64encode(data).rstrip(b"=").decode()


DEV_ADMIN_USER_ID = "00000000-0000-7000-8000-0000000000a1"
DEV_JWT_SECRET = (
    "osa-local-dev-jwt-secret-CHANGE-IN-PRODUCTION-not-suitable-for-real-use"
)


def _mint_dev_token() -> str:
    header = json.dumps({"alg": "HS256", "typ": "JWT"}, separators=(",", ":"))
    payload = json.dumps(
        {
            "sub": DEV_ADMIN_USER_ID,
            "provider": "local",
            "external_id": "admin@osa.local",
            "aud": "authenticated",
            "iat": int(time.time()),
            "exp": int(time.time()) + 86400 * 365,
            "jti": secrets.token_hex(16),
        },
        separators=(",", ":"),
    )
    header_b64 = _b64url(header.encode())
    payload_b64 = _b64url(payload.encode())
    message = f"{header_b64}.{payload_b64}".encode()
    signature = hmac.new(DEV_JWT_SECRET.encode(), message, hashlib.sha256).digest()
    return f"{header_b64}.{payload_b64}.{_b64url(signature)}"


def _store_dev_credentials() -> None:
    """Mint a dev token signed with the well-known dev secret, store it."""
    token = _mint_dev_token()

    from osa.cli.credentials import write_credentials

    write_credentials(
        "http://127.0.0.1:8000",
        access_token=token,
        refresh_token="dev-no-refresh",
    )


